def is_cpuple(a,b):
    return (a == '(' and b == ')') or (a == '[' and b == ']') or (a == '{' and b == '}')


def solution(S):

    q = []
    for s in S:
        print(q)
        if s in ['(','[','{']:
            q.append(s)
        else:
            if len(q) == 0:
                return 0
            if is_cpuple(q[-1],s):
                q.pop(-1)
            else:
                return 0

    if len(q)==0:
        return 1
    return 0
